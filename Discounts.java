    private void jTable2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable2KeyPressed
        // TODO add your handling code here:
        try {
            int keyCode = evt.getKeyCode();
            if(keyCode == KeyEvent.VK_DELETE && jTable2.getSelectedRow() == -1) {
                fillInsertInfo();
                fillTable();
                JOptionPane.showMessageDialog(this, "You Need To Select an Existing Discount", "Operation Failure", 0);
            }

            else if(keyCode == KeyEvent.VK_DELETE && jTable2.getSelectedRow() != -1) {
                if(String.valueOf(m.getValueAt(jTable2.getSelectedRow(), 0)).equals("11")) {
                    JOptionPane.showMessageDialog(this, "Discount Cannot Be Deleted", "Operation Failure", 0);
                }
                else {
                    int result = JOptionPane.showConfirmDialog(this, "Do you wish to continue?\nThis process cannot be undone.", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
                    if(result == 0){
                        deleteDiscount();
                        fillInsertInfo();
                        fillTable();
                        JOptionPane.showMessageDialog(this, "Discount Deleted Successfully", "Operation Success", 1);
                    }
                }
            }
        }
        catch (Exception e) {

        }
    }//GEN-LAST:event_jTable2KeyPressed

