    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        try {
            if(company1.getText().isBlank() ||
               discount1.getText().isBlank()) {

                JOptionPane.showMessageDialog(this, "Blank Spots Encountered", "Operation Not Successful", 0);
            }
            else {
                int row = jTable2.getSelectedRow();

                if(String.valueOf(m.getValueAt(jTable2.getSelectedRow(), 0)).equals("11")) {
                    JOptionPane.showMessageDialog(this, "Discount Cannot Be Updated", "Operation Failure", 0);
                }
                else {
                    try {
                        double value = (double) Math.round(Double.parseDouble(discount1.getText()) * 100) / 100;
                        discount1.setText(String.valueOf(value));

                        String Company1 = company1.getText();
                        String Discount1 = discount1.getText();

                        String str = "0123456789";
                        String[] arr;
                        boolean flag = false;
                        for (int i = 0; i < str.length(); i++) {
                            arr = str.split("");
                            if(Company1.contains(arr[i]))
                            {
                                flag = true;
                            }
                        }

                        if(flag) {
                            JOptionPane.showMessageDialog(this, "Company Name Does Not Accept Digits", "Constraint", 0);
                        }
                        else {
                            getData.clear();

                            getData = DB_Connection.selectWhere4(new String[] {"Company"}, new String[]{"Company"}, new String[] {Company1}, "Discounts");
                            String existingDT = "";
                            if(!getData.isEmpty()) {
                                existingDT = getData.get(0);
                            }

                            getData.clear();

                            String discountID = String.valueOf(m.getValueAt(row, 0));

                            if (Company1.equals(String.valueOf(m.getValueAt(row, 1)))){
                                String[] columns1 = {"Company", "Discount"};
                                String[] values1 = {Company1, Discount1};

                                String[] whereColumns1 = {"DiscountID"};
                                String[] whereValues1 = {discountID};

                                DB_Connection.updateString(columns1, values1, whereColumns1, whereValues1, "Discounts");

                                fillInsertInfo();
                                fillTable();

                                JOptionPane.showMessageDialog(this, "Operation Completed Successfully");
                            }
                            else if(existingDT.equals(Company1)) {
                                JOptionPane.showMessageDialog(this, "Repeating Discount", "Constraint", 0);
                            }
                            else {
                                String[] columns1 = {"Company", "Discount"};
                                String[] values1 = {Company1, Discount1};

                                String[] whereColumns1 = {"DiscountID"};
                                String[] whereValues1 = {discountID};

                                DB_Connection.updateString(columns1, values1, whereColumns1, whereValues1, "Discounts");

                                fillInsertInfo();
                                fillTable();

                                JOptionPane.showMessageDialog(this, "Operation Completed Successfully");
                            }
                        }
                    }
                    catch (Exception e){
                        JOptionPane.showMessageDialog(this, "Discount Not In Correct Format", "Discount Constraint", 0);
                    }
                }
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "You Need To Select a Discount", "Operation Failure", 0);
        }
    }//GEN-LAST:event_UpdateActionPerformed

