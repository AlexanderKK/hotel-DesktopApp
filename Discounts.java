    private void deleteDiscount() {
        int row = jTable2.getSelectedRow();
        String discountID = String.valueOf(m.getValueAt(row, 0));

        //Reservations Deletion
        getData.clear();

        //Get Reservations ID
        String[] columns1 = {"Reservation_ID"};
        String[] whereColumns1 = {"DiscountID"};
        String[] whereValues1 = {discountID};
        String table1 = "Reservations";
        getData = DB_Connection.selectWhere3(columns1, whereColumns1, whereValues1, table1);

        for (int i = 0; i < getData.size(); i++) {
            String resID = getData.get(i);

            //Update Reservations by DiscountID
            String[] columns2 = {"DiscountID"};
            String[] values2 = {"11"};
            String[] whereColumns2 = {"Reservation_ID", "DiscountID"};
            String[] whereValues2 = {resID, discountID};
            String table2 = "Reservations";
            DB_Connection.updateString(columns2, values2, whereColumns2, whereValues2, table2);
        }

        getData.clear();

        //Discount Deletion
        String[] whereColumns3 = {"DiscountID"};
        String[] whereValues3 = {discountID};
        String table3 = "Discounts";
        DB_Connection.delete(whereColumns3, whereValues3, table3);

        getData.clear();
        getData1.clear();
    }
    
